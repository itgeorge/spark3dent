            var result = Transcribe(cents);
            Assert.That(result, Contains.Substring($"–µ–≤—Ä–æ"));
            if (cents % 100 == 0) 
            {
                Assert.That(result, Does.Not.Contain($"–µ–≤—Ä–æ—Ü–µ–Ω—Ç"));
            } 
            else 
            {
                Assert.That(result, Contains.Substring("–µ–≤—Ä–æ—Ü–µ–Ω—Ç"));
            }

            var getExceptionMessage = () => $"Failed for {cents} cents";
            if (cents / 1_000_00 > 0)
            {
                Assert.That(result.ToLower(), Contains.Substring("—Ö–∏–ª—è–¥–∞").Or.Contains("—Ö–∏–ª—è–¥–∏"), getExceptionMessage);
            }
            else if (cents / 100_00 > 0)
            {
                Assert.That(result.ToLower(), Contains.Substring("—Å—Ç–æ").Or.Contains("—Å—Ç–∞"), getExceptionMessage);
            }
            else if (cents / 10_00 > 0)
            {
                Assert.That(result.ToLower(), Contains.Substring("–¥–µ—Å–µ—Ç"), getExceptionMessage);
            }

            Assert.That(result, Is.Not.Null.And.Not.Empty, getExceptionMessage);
        }
    }
    
    [Test]
    public void Transcribe_WhenTranscribingAnyNumberBelow1M_ShouldSatisfyLinguisticInvariants()
    {
        for (var euros = 1; euros <= 999_999; euros++)
        {
            var cents = euros * 100;
            var result = Transcribe(cents).ToLower();

            string fail(string msg) => $"{msg} (failed for {cents} cents ‚Üí \"{result}\")";

            // 1Ô∏è‚É£ Basic sanity
            Assert.That(result, Is.Not.Null.And.Not.Empty, fail("Result is null/empty"));

            // 2Ô∏è‚É£ Must always contain "–µ–≤—Ä–æ"
            Assert.That(result, Does.Contain("–µ–≤—Ä–æ"), fail("Missing '–µ–≤—Ä–æ'"));

            // 3Ô∏è‚É£ Zero cents rule
            if (cents % 100 == 0)
                Assert.That(result, Does.Not.Contain("–µ–≤—Ä–æ—Ü–µ–Ω—Ç"), fail("Contains eurocent when cents == 0"));
            else
                Assert.That(result, Does.Contain("–µ–≤—Ä–æ—Ü–µ–Ω—Ç"), fail("Missing eurocent when cents != 0"));

            // 4Ô∏è‚É£ Never allow illegal currency forms
            Assert.That(result, Does.Not.Contain("–µ–≤—Ä–∞"), fail("Illegal plural '–µ–≤—Ä–∞' detected"));
            Assert.That(result, Does.Not.Contain("–µ–≤—Ä–æ—Ü–µ–Ω—Ç–æ–≤–µ"), fail("Wrong plural '–µ–≤—Ä–æ—Ü–µ–Ω—Ç–æ–≤–µ' detected"));

            // 5Ô∏è‚É£ No double conjunction
            Assert.That(result, Does.Not.Contain(" –∏ –∏ "), fail("Double '–∏' detected"));

            // 6Ô∏è‚É£ No trailing conjunction
            Assert.That(result.Trim().EndsWith("–∏"), Is.False, fail("Ends with '–∏'"));

            // 7Ô∏è‚É£ No double spaces
            Assert.That(result, Does.Not.Contain("  "), fail("Double space detected"));

            // 8Ô∏è‚É£ Gender invariant: neuter for euro
            if (euros % 100 != 11) // exclude 11 because it ends with "–µ–¥–∏–Ω–∞–¥–µ—Å–µ—Ç"
            {
                if (euros % 10 == 1)
                    Assert.That(result, Does.Not.Contain("–µ–¥–∏–Ω –µ–≤—Ä–æ"),
                        fail("Masculine '–µ–¥–∏–Ω –µ–≤—Ä–æ' detected"));
                if (euros % 10 == 2)
                    Assert.That(result, Does.Not.Contain("–¥–≤–∞ –µ–≤—Ä–æ"),
                        fail("Masculine '–¥–≤–∞ –µ–≤—Ä–æ' detected"));
            }

            // 9Ô∏è‚É£ Gender invariant: masculine for eurocent
            var centPart = cents % 100;
            if (centPart == 1)
                Assert.That(result, Does.Contain("–µ–¥–∏–Ω –µ–≤—Ä–æ—Ü–µ–Ω—Ç"),
                    fail("Missing masculine singular '–µ–¥–∏–Ω –µ–≤—Ä–æ—Ü–µ–Ω—Ç'"));
            if (centPart == 2)
                Assert.That(result, Does.Contain("–¥–≤–∞ –µ–≤—Ä–æ—Ü–µ–Ω—Ç–∞"),
                    fail("Missing masculine plural '–¥–≤–∞ –µ–≤—Ä–æ—Ü–µ–Ω—Ç–∞'"));

            // üîü Thousand agreement check
            if (euros >= 2000 && euros < 3000)
                Assert.That(result, Does.Contain("–¥–≤–µ —Ö–∏–ª—è–¥–∏"),
                    fail("Should use feminine '–¥–≤–µ —Ö–∏–ª—è–¥–∏'"));

            if (euros >= 1000 && euros < 2000)
                Assert.That(result, Does.Contain("—Ö–∏–ª—è–¥–∞"),
                    fail("Should contain '—Ö–∏–ª—è–¥–∞'"));

            // 1Ô∏è‚É£1Ô∏è‚É£ No uppercase mid-sentence (except first char)
            Assert.That(result.Substring(1),
                Does.Not.Match("[–ê-–Ø]"),
                fail("Unexpected uppercase inside sentence"));
        }
    }


